
public class Decimal {
	
	public String decToBin(int input){
		String output;
		output = Integer.toString(input,2);
		System.out.println(output);
	return output;
}
	public String decToHex(int input){
		String output;
		output = Integer.toString(input,16);
		System.out.println(output);
	return output;
	}
}
